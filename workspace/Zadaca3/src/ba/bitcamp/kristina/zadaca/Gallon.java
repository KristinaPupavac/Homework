/*
 * Task 1
Napisati program koji konvertira litre u galone 
(isti princip kao što je radjen na zadnjem dijelu vježbi).)

 */

public class Gallon {

	public static void main(String[] args) {
		
		int litar = 35;
		
		double gallon = litar * 1 / 3.785412;
		
		System.out.println(gallon + " [gal]");
	}

}
